            <p>L'histoire commence pendant l'adolescence de Naruto, vers ses douze ans. Orphelin cancre et grand farceur, il fait toutes les bêtises possibles pour se faire remarquer. Son rêve : devenir Hokage afin d'être reconnu par les habitants de son village. En effet, le démon renard à neuf queues scellé en lui a attisé la crainte et le mépris des autres villageois, qui, avec le temps, ne font plus de différence entre Kyûbi et Naruto. Malgré cela, Naruto s'entraîne dur afin de devenir genin, le premier niveau chez les ninjas. Après plusieurs essais, il arrive finalement à recevoir son bandeau frontal de Konoha et la promotion qui va avec. Il est alors inclus dans une équipe de trois apprentis ninjas, avec Sakura Haruno et le talentueux Sasuke Uchiwa. Peu après, ils rencontrent leur jōnin, celui qui s'occupera de leur formation : le mystérieux Kakashi Hatake.</p>

            <p>Au début craint et méprisé par ses pairs, il va peu à peu monter en puissance et gagner le respect et l'affection des villageois grâce notamment aux combats dantesques qu'il remportera face aux ennemis les plus puissants de Konoha.</p>
