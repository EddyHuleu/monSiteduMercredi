            <h2 class="blog-post-title">Prologue</h2>
            <p class="blog-post-meta">Mercredi 3 Mai, 2017 par <a href="#">Robert</a></p>

            <p>L'histoire de Naruto se déroule dans un monde rétro-futuriste où, bien que de nombreuses technologies modernes aient vu le jour, les ninjas et, dans une moindre mesure, les samouraïs sont restés de véritables puissances militaires. Chaque pays a un village, qui représente la force militaire du pays, dirigé par un Kage. Les villages, à travers leurs ninjas, se livrent des guerres les uns aux autres, à petite ou grande échelle, que ce soit pour obtenir des caractéristiques avantageuses propres aux villages ennemis, ou pour soumettre un autre village et gagner en puissance.</p>
            
            <p>Dans ce monde ninja, il existe neuf créatures gigantesques, appelé les Démons à queues, qui peuvent être scellés dans un humain après une cérémonie sacrificielle. L'humain devient alors un hôte et dispose alors de la puissance du démon. Douze années avant le début du récit, l'une de ces créatures attaque le village du Pays du Feu, Konoha. Elle est capturée et scellée dans un nouveau-né nommé Naruto Uzumaki par le quatrième Hokage, faisant de lui l'hôte du démon à neuf queues, Kyûbi.</p>
